void display_distance(uint8_t num)
{
	char buf[11];
	sprintf(buf, "%d       ", num);

	SSD1306_GotoXY (70,10);
	SSD1306_Puts (buf, &Font_11x18, 1);
	SSD1306_UpdateScreen();
}

